#include "fillit.h"

int		ft_strijcheck(char *str)
{
	int		i;
	int		p;
	int		j;

	i = 0;
	p = 12;
	j = 4;
	while (i < 20)
	{
		if (str[i] != '.' && str[i] != '#' && str[i] != '\n')
		{
			return (0);
		}
		else
		{
			if (str[i] == '.')
				p--;
			if (str[i] == '#')
				j--;
			i++;
		}
	}
	if (!(j == 0 && p == 0))
		return (0);
	return (1);
}
