#include "fillit.h"

int			ft_slashcheck(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i < 21)
	{
		i++;
		j++;
		if (str[i] == '\n' && j == 5)
			j = 0;
		else
			return (0);
	}
	if (str[i + 1] == '\n')
		return (1);
	return (0);
}
