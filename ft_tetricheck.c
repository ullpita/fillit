#include "fillit.h"

int		ft_tetricheck(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i <= 20)
	{
		if (str[i] == '#')
		{
			if (str[i + 1] == '#' || str[i - 1] == '#' || str[i + 5] == '#' ||
					str[i - 5] == '#')
				j++;
			if (j == 0)
				return (0);
			j = 0;
		}
		i++;
	}
	return (1);
}
